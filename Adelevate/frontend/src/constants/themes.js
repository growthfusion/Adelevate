// ============ HELPER FUNCTIONS ============

/**
 * Get metric color based on current theme
 * @param {string} metricKey - The metric key (e.g., 'revenue', 'clicks')
 * @param {boolean} isDarkMode - Whether dark mode is active
 * @returns {string} - The color hex code
 */
export const getMetricColor = (metricKey, isDarkMode) => {
  const colors = metricColors[metricKey];
  if (!colors) return isDarkMode ? "#3B82F6" : "#2563EB";
  return isDarkMode ? colors.dark : colors.light;
};

/**
 * Get platform color
 * @param {string} platform - The platform name
 * @returns {object} - Platform color object with primary, light, and glow
 */
export const getPlatformColor = (platform) => {
  return platformColors[platform] || platformColors.facebook;
};

/**
 * Create CSS variables from theme
 * @param {object} theme - Theme object
 * @returns {string} - CSS variables string
 */
export const createCSSVariables = (theme) => {
  let css = ":root {\n";
  Object.entries(theme).forEach(([key, value]) => {
    if (typeof value === "string") {
      css += `  --${key}: ${value};\n`;
    }
  });
  css += "}";
  return css;
};
